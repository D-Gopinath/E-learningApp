package com.elearning.registration;

public class Registration {
	public String name;
	public String phone;
	public String email;
	public String password;
	public Registration(String name, String phone, String email, String password) {
		super();
		this.name = name;
		this.phone = phone;
		this.email = email;
		this.password = password; 
	}	
}
